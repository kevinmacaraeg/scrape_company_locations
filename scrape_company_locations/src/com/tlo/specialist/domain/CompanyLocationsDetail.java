package com.tlo.specialist.domain;

public class CompanyLocationsDetail {
	
	private String masterCompanyId;
	
	private String masterCompanyName;
	
	private String website;
	
	private String employeeSize;
	
	private String companyLocationsUrl;
	
	private String companyLocationsWebsitePageStructure;
	
	private String companyLocationsCssSelector;

	public String getMasterCompanyId() {
		return masterCompanyId;
	}

	public void setMasterCompanyId(String masterCompanyId) {
		this.masterCompanyId = masterCompanyId;
	}

	public String getMasterCompanyName() {
		return masterCompanyName;
	}

	public void setMasterCompanyName(String masterCompanyName) {
		this.masterCompanyName = masterCompanyName;
	}

	public String getWebsite() {
		return website;
	}

	public void setWebsite(String website) {
		this.website = website;
	}

	public String getEmployeeSize() {
		return employeeSize;
	}

	public void setEmployeeSize(String employeeSize) {
		this.employeeSize = employeeSize;
	}

	public String getCompanyLocationsUrl() {
		return companyLocationsUrl;
	}

	public void setCompanyLocationsUrl(String companyLocationsUrl) {
		this.companyLocationsUrl = companyLocationsUrl;
	}

	public String getCompanyLocationsWebsitePageStructure() {
		return companyLocationsWebsitePageStructure;
	}

	public void setCompanyLocationsWebsitePageStructure(String companyLocationsWebsitePageStructure) {
		this.companyLocationsWebsitePageStructure = companyLocationsWebsitePageStructure;
	}

	public String getCompanyLocationsCssSelector() {
		return companyLocationsCssSelector;
	}

	public void setCompanyLocationsCssSelector(String companyLocationsCssSelector) {
		this.companyLocationsCssSelector = companyLocationsCssSelector;
	}
	
}
